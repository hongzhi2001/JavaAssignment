    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        String identityNo = txtIdenNo.getText();
        String pass = txtPassword.getText();
        
        if(identityNo.trim().isEmpty()|| pass.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please ensure all required fields have been fill");
            return;
        }                
        People found = DataIO.checkPeople(identityNo);
        if(found==null){
            JOptionPane.showMessageDialog(this, "Invalid Input");     
        }else{
            if(pass.equals(found.getPhoneNo())){
                JavaAssignment.login=found;
                txtIdenNo.setText("");
                txtPassword.setText("");               
                JavaAssignment.h.setPeople(found.getName());
                JavaAssignment.h.checkStatus(found);
                JavaAssignment.h.setPeopleStatus(found.getStatus());
                JavaAssignment.pm.getProfile(found);
                JavaAssignment.va.viewAppointment(found);
                //JavaAssignment.vs.viewVaccine();
                //JavaAssignment.t.test();
                this.setVisible(false);
                JavaAssignment.h.setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(this, "Wrong Password!");
            }
        }
    }//GEN-LAST:event_BtnLoginActionPerformed

